# Inicialize uma lista para armazenar as médias de cada arquivo
medias_milissegundos_plmn = []

###################################################################################
# Processa cada arquivo
for nome_arquivo in nomes_arquivos_plmn:
    tempos = []
    with open(nome_arquivo, 'r') as arquivo:
        for linha in arquivo:
            tempo = float(linha.strip())  # Converte a linha em um número de ponto flutuante
            tempos.append(tempo)

    # Cálculo da média em milissegundos
    media_segundos = sum(tempos) / len(tempos)
    media_milissegundos = media_segundos * 1000
    medias_milissegundos_plmn.append(media_milissegundos)

# Calcula a média das médias
media_total_milissegundos = sum(medias_milissegundos_plmn) / len(medias_milissegundos_plmn)

# Salva o resultado em um arquivo de texto
with open('media_resultado_plmn_thread.txt', 'w') as resultado_arquivo:
    for media in medias_milissegundos_plmn:
        resultado_arquivo.write(f'{media:.12f}\n')
    resultado_arquivo.write(f'Média Total em Milissegundos: {media_total_milissegundos:.12f}\n')

print(f'Médias calculadas e resultado salvo em "media_resultado_plmn_thread.txt"')

